export function getClassSkills(className: string): ActiveSkill[] {
  const classKey = className.toLowerCase() as keyof typeof ACTIVE_SKILLS
  return ACTIVE_SKILLS[classKey] || []
}

// Ğ¤ÑƒĞ½ĞºÑ†Ğ¸Ñ Ğ´Ğ»Ñ Ğ¿Ğ¾Ğ»ÑƒÑ‡ĞµĞ½Ğ¸Ñ Ğ´Ğ¾ÑÑ‚ÑƒĞ¿Ğ½Ñ‹Ñ… Ğ½Ğ°Ğ²Ñ‹ĞºĞ¾Ğ² Ğ¿Ğ¾ ÑƒÑ€Ğ¾Ğ²Ğ½Ñ
export function getAvailableSkills(className: string, level: number): ActiveSkill[] {
  const skills = getClassSkills(className)
  return skills.map(skill => ({
    ...skill,
    is_learned: skill.level_requirement <= level
  }))
}
